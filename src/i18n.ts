/** 国际化 (i18n) - 根据系统语言自动切换 TUI 文本 */

let forceLocale: string | null = null;

export function setLocale(lang: string | null): void {
  forceLocale = lang;
}

export function getLocale(): string {
  if (forceLocale !== null) return forceLocale;
  return detectLocale();
}

function detectLocale(): string {
  const envLang = process.env.LANG || process.env.LANGUAGE || process.env.LC_ALL || process.env.LC_MESSAGES;
  if (envLang && (envLang.toLowerCase().startsWith('zh') || envLang.toLowerCase().includes('zh_'))) {
    return 'zh';
  }
  try {
    const intlLocale = Intl.DateTimeFormat().resolvedOptions().locale;
    if (intlLocale.toLowerCase().startsWith('zh')) return 'zh';
  } catch {
    // ignore
  }
  return 'en';
}

// eslint-disable-next-line @typescript-eslint/no-explicit-any
export function formatTemplate(text: string, kwargs: Record<string, any>): string {
  let result = text;
  for (const [key, value] of Object.entries(kwargs)) {
    result = result.replace(new RegExp(`\\{${key}\\}`, 'g'), String(value));
  }
  return result;
}

